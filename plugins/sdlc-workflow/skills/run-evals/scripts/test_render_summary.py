#!/usr/bin/env python3
"""Tests for render_summary.py version footer functionality.

Verifies that:
- The --version flag appends a version footer to the rendered output
- Omitting --version produces no footer (backward compatible)
"""

import json
import subprocess
import sys
import tempfile
from pathlib import Path

SCRIPT = Path(__file__).parent / "render_summary.py"
FOOTER_MARKER = "---\n*Generated by [sdlc-workflow/run-evals](https://github.com/RHEcosystemAppEng/sdlc-plugins)"


def make_minimal_results(tmpdir: Path) -> Path:
    """Create a minimal results directory with one passing eval."""
    results = tmpdir / "results"
    eval_dir = results / "eval-1"
    eval_dir.mkdir(parents=True)
    grading = {
        "assertion_results": [
            {"text": "sample assertion", "passed": True, "evidence": "ok"}
        ],
        "summary": {"passed": 1, "failed": 0, "total": 1, "pass_rate": 1.0},
    }
    (eval_dir / "grading.json").write_text(json.dumps(grading))
    return results


def test_version_flag_appends_footer():
    """Verify that --version appends the version footer to the output."""
    with tempfile.TemporaryDirectory() as tmpdir:
        results = make_minimal_results(Path(tmpdir))
        output = Path(tmpdir) / "summary.md"

        proc = subprocess.run(
            [sys.executable, str(SCRIPT), "--results", str(results),
             "--version", "1.2.3", "--output", str(output)],
            capture_output=True, text=True,
        )
        assert proc.returncode == 0, f"Script failed: {proc.stderr}"

        content = output.read_text()
        assert content.endswith(
            "---\n"
            "*Generated by [sdlc-workflow/run-evals]"
            "(https://github.com/RHEcosystemAppEng/sdlc-plugins) v1.2.3*\n"
            "\n"
        ), f"Footer not found at end of output:\n{content!r}"


def test_no_version_flag_no_footer():
    """Verify that omitting --version produces no footer."""
    with tempfile.TemporaryDirectory() as tmpdir:
        results = make_minimal_results(Path(tmpdir))
        output = Path(tmpdir) / "summary.md"

        proc = subprocess.run(
            [sys.executable, str(SCRIPT), "--results", str(results),
             "--output", str(output)],
            capture_output=True, text=True,
        )
        assert proc.returncode == 0, f"Script failed: {proc.stderr}"

        content = output.read_text()
        assert FOOTER_MARKER not in content, (
            f"Footer should not be present without --version:\n{content!r}"
        )


def test_version_footer_format():
    """Verify the exact footer format matches the expected pattern."""
    with tempfile.TemporaryDirectory() as tmpdir:
        results = make_minimal_results(Path(tmpdir))
        output = Path(tmpdir) / "summary.md"

        proc = subprocess.run(
            [sys.executable, str(SCRIPT), "--results", str(results),
             "--version", "0.13.4", "--output", str(output)],
            capture_output=True, text=True,
        )
        assert proc.returncode == 0, f"Script failed: {proc.stderr}"

        lines = output.read_text().rstrip("\n").split("\n")
        assert lines[-2] == "---", f"Expected '---' separator, got: {lines[-2]!r}"
        assert lines[-1] == (
            "*Generated by [sdlc-workflow/run-evals]"
            "(https://github.com/RHEcosystemAppEng/sdlc-plugins) v0.13.4*"
        ), f"Unexpected footer line: {lines[-1]!r}"


if __name__ == "__main__":
    tests = [
        test_version_flag_appends_footer,
        test_no_version_flag_no_footer,
        test_version_footer_format,
    ]
    failed = 0
    for t in tests:
        try:
            t()
            print(f"  PASS  {t.__name__}")
        except AssertionError as e:
            print(f"  FAIL  {t.__name__}: {e}")
            failed += 1

    if failed:
        print(f"\n{failed}/{len(tests)} tests failed")
        sys.exit(1)
    else:
        print(f"\nAll {len(tests)} tests passed")
